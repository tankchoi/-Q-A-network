    public Main(int Role, int userID) {
        this.role = Role;
        this.userID = userID;
        Main mainReference = this;
        initComponents();
        getContentPane().setBackground(new Color(255, 255, 255));
        MenuEvent event = new MenuEvent() {
            @Override
            public void menuSelected(int index) {
                if (index == 0) {
                    showForm(new FormHome());
                } else if (index == 1) {
                    showForm(new Staff(Role));
                } else if (index == 3) {
                    showForm(new Accounting(Role));
                } else if (index == 4) {
                    showForm(new Categories());
                } else if (index == 5) {
                    Posts postsForm = new Posts();
                    postsForm.setMain(mainReference);
                    showForm(postsForm);
                } else if (index == 2) {
                    showForm(new ApproveCate(userID));
                } else if (index == 6) {
                    showForm(new ChangePassword(userID));
                } else {
                    Window window = SwingUtilities.getWindowAncestor(body);
                    if (window instanceof JFrame) {
                        ((JFrame) window).dispose();
                    }
                    new Login().setVisible(true);
                }
            }
        };
        menu.initMenu(event);
        menu.setSelected(0);
    }

